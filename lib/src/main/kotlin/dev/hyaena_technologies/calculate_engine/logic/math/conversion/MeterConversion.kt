package dev.hyaena_technologies.calculate_engine.logic.math.conversion

// Meter Conversion

// Centimeters to Meters
fun CentimetersToMeters(centimeters: Double): Double {
    return centimeters * 0.01
}

// Decameters to Meters
fun DecametersToMeters(decameters: Double): Double {
    return decameters * 10
}

// Decimeters to Meters
fun DecimetersToMeters(decimeters: Double): Double {
    return decimeters * 0.1
}

// Feet to Meters
fun FeetToMeters(feet: Double): Double {
    return feet * 0.3048
}

// Hectometers to Meters
fun HectometersToMeters(hectometers: Double): Double {
    return hectometers * 100
}

// Inches to Meters
fun InchesToMeters(inches: Double): Double {
    return inches * 0.0254
}

// Kilometers to Meters
fun KilometersToMeters(kilometers: Double): Double {
    return kilometers * 1000
}

// Micrometers to Meters
fun MicrometersToMeters(micrometers: Double): Double {
    return micrometers * -1000000
}

// Miles to Meters
fun MilesToMeters(miles: Double): Double {
    return miles * 1609.344
}

// Millimeters to Meters
fun MillimetersToMeters(millimeters: Double): Double {
    return millimeters * 0.001
}

// Nanometers to Meters
fun NanometersToMeters(nanometers: Double): Double {
    return nanometers * -1000000000
}

// Nautical Miles to Meters
fun NauticalMilesToMeters(nauticalMiles: Double): Double {
    return nauticalMiles * 1852.001
}

// Picometers to Meters
fun PicometersToMeters(picometers: Double): Double {
    return picometers * -1000000000000
}

// Yards to Meters
fun YardsToMeters(yards: Double): Double {
    return yards * 0.9144
}