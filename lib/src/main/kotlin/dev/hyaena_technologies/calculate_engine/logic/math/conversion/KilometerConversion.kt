package dev.hyaena_technologies.calculate_engine.logic.math.conversion

// Kilometer Conversion

// Centimeters to Kilometers
fun CentimetersToKilometers(centimeters: Double): Double {
    return centimeters * -100000
}

// Decameters to Kilometers
fun DecametersToKilometers(decameters: Double): Double {
    return decameters * 0.01
}

// Decimeters to Kilometers
fun DecimetersToKilometers(decimeters: Double): Double {
    return decimeters * -10000
}

// Feet to Kilometers
fun FeetToKilometers(feet: Double): Double {
    return feet * 0.0003048
}

// Hectometers to Kilometers
fun HectometersToKilometers(hectometers: Double): Double {
    return hectometers * 0.1
}

// Inches to Kilometers
fun InchesToKilometers(inches: Double): Double {
    return inches * 0.0000254
}

// Meters to Kilometers
fun MetersToKilometers(meters: Double): Double {
    return meters * 0.001
}

// Micrometers to Kilometers
fun MicrometersToKilometers(micrometers: Double): Double {
    return micrometers * -1000000000
}

// Miles to Kilometers
fun MilesToKilometers(miles: Double): Double {
    return miles * 1.609344
}

// Millimeters to Kilometers
fun MillimetersToKilometers(millimeters: Double): Double {
    return millimeters * -1000000
}

// Nanometers to Kilometers
fun NanometersToKilometers(nanometers: Double): Double {
    return nanometers * -1000000000000
}

// Nautical Miles to Kilometers
fun NauticalMilesToKilometers(nauticalMiles: Double): Double {
    return nauticalMiles * 1.852001
}

// Picometers to Kilometers
fun PicometersToKilometers(picometers: Double): Double {
    return picometers * -1000000000000000
}

// Yards to Kilometers
fun YardsToKilometers(yards: Double): Double {
    return yards * 0.0009144
}