package dev.calculate_engine.logic.math.conversion

// Decimeter Conversion

// Centimeters to Decimeters
fun centimetersToDecimeters(centimeters: Double): Double {
    return centimeters * 0.1
}

// Decameters to Decimeters
fun decametersToDecimeters(decameters: Double): Double {
    return decameters * 100
}

// Feet to Decimeters
fun feetToDecimeters(feet: Double): Double {
    return feet * 3.048
}

// Hectometers to Decimeters
fun hectometersToDecimeters(hectometers: Double): Double {
    return hectometers * 1000
}

// Inches to Decimeters
fun inchesToDecimeters(inches: Double): Double {
    return inches * 0.254
}

// Kilometers to Decimeters
fun kilometersToDecimeters(kilometers: Double): Double {
    return kilometers * 10000
}

// Meters to Decimeters
fun metersToDecimeters(meters: Double): Double {
    return meters * 10
}

// Micrometers to Decimeters
fun micrometersToDecimeters(micrometers: Double): Double {
    return micrometers * -100000
}

// Miles to Decimeters
fun milesToDecimeters(miles: Double): Double {
    return miles * 16093.44
}

// Millimeters to Decimeters
fun millimetersToDecimeters(millimeters: Double): Double {
    return millimeters * 0.01
}

// Nanometers to Decimeters
fun nanometersToDecimeters(nanometers: Double): Double {
    return nanometers * -100000000
}

// Nautical Miles to Decimeters
fun nauticalMilesToDecimeters(nauticalMiles: Double): Double {
    return nauticalMiles * 18520.01
}

// Picometers to Decimeters
fun picometersToDecimeters(picometers: Double): Double {
    return picometers * -100000000000
}

// Yards to Decimeters
fun yardsToDecimeters(yards: Double): Double {
    return yards * 9.144
}