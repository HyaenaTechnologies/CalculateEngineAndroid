package dev.calculate_engine.math.conversion

// Millimeter Conversion

// Centimeters to Millimeters
fun centimetersToMillimeters(centimeters: Double): Double {
    return centimeters * 10
}

// Decameters to Millimeters
fun decametersToMillimeters(decameters: Double): Double {
    return decameters * 10000
}

// Decimeters to Millimeters
fun decimetersToMillimeters(decimeters: Double): Double {
    return decimeters * 100
}

// Feet to Millimeters
fun feetToMillimeters(feet: Double): Double {
    return feet * 304.8
}

// Hectometers to Millimeters
fun hectometersToMillimeters(hectometers: Double): Double {
    return hectometers * 100000
}

// Inches to Millimeters
fun inchesToMillimeters(inches: Double): Double {
    return inches * 25.4
}

// Kilometers to Millimeters
fun kilometersToMillimeters(kilometers: Double): Double {
    return kilometers * 1000000
}

// Meters to Millimeters
fun metersToMillimeters(meters: Double): Double {
    return meters * 1000
}

// Micrometers to Millimeters
fun micrometersToMillimeters(micrometers: Double): Double {
    return micrometers * 0.001
}

// Miles to Millimeters
fun milesToMillimeters(miles: Double): Double {
    return miles * 1609344
}

// Nanometers to Millimeters
fun nanometersToMillimeters(nanometers: Double): Double {
    return nanometers * -1000000
}

// Nautical Miles to Millimeters
fun nauticalMilesToMillimeters(nauticalMiles: Double): Double {
    return nauticalMiles * 1852001
}

// Picometers to Millimeters
fun picometersToMillimeters(picometers: Double): Double {
    return picometers * -1000000000
}

// Yards to Millimeters
fun yardsToMillimeters(yards: Double): Double {
    return yards * 914.4
}