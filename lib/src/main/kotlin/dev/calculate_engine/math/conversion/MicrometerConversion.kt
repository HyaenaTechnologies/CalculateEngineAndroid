package dev.calculate_engine.math.conversion

// Micrometer Conversion

// Centimeters to Micrometers
fun centimetersToMicrometers(centimeters: Double): Double {
    return centimeters * 10000
}

// Decameters to Micrometers
fun decametersToMicrometers(decameters: Double): Double {
    return decameters * 10000000
}

