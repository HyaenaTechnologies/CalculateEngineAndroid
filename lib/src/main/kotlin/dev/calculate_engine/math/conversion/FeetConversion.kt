package dev.calculate_engine.math.conversion

// Feet Conversion

// Centimeters to Feet
fun centimetersToFeet(centimeters: Double): Double {
    return centimeters * 0.0328084
}

// Decameters to Feet
fun decametersToFeet(decameters: Double): Double {
    return decameters * 32.8084
}

// Decimeters to Feet
fun decimetersToFeet(decimeters: Double): Double {
    return decimeters * 0.328084
}

// Hectometers to Feet
fun hectometersToFeet(hectometers: Double): Double {
    return hectometers * 328.084
}

// Inches to Feet
fun inchesToFeet(inches: Double): Double {
    return inches * 0.08333333
}

// Kilometers to Feet
fun kilometersToFeet(kilometers: Double): Double {
    return kilometers * 3280.84
}

// Meters to Feet
fun metersToFeet(meters: Double): Double {
    return meters * 3.28084
}

// Micrometers to Feet
fun micrometersToFeet(micrometers: Double): Double {
    return micrometers * 0.00000328084
}

// Miles to Feet
fun milesToFeet(miles: Double): Double {
    return miles * 5280
}

// Millimeters to Feet
fun millimetersToFeet(millimeters: Double): Double {
    return millimeters * 0.00328084
}

// Nanometers to Feet
fun nanometersToFeet(nanometers: Double): Double {
    return nanometers * 0.00000000328084
}

// Nautical Miles to Feet
fun nauticalMilesToFeet(nauticalMiles: Double): Double {
    return nauticalMiles * 6076.118
}

// Picometers to Feet
fun picometersToFeet(picometers: Double): Double {
    return picometers * 0.00000000000328084
}

// Yards to Feet
fun yardsToFeet(yards: Double): Double {
    return yards * 3
}