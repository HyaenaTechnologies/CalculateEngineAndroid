package dev.calculate_engine.math.conversion

// Nanometer Conversion

// Centimeters to Nanometers
fun centimetersToNanometers(centimeters: Double): Double {
    return centimeters * 10000000
}

// Decameters to Nanometers
fun decametersToNanometers(decameters: Double): Double {
    return decameters * 10000000000
}

// Decimeters to Nanometers
fun decimetersToNanometers(decimeters: Double): Double {
    return decimeters * 100000000
}

// Feet to Nanometers
fun feetToNanometers(feet: Double): Double {
    return feet * 304800000
}

// Hectometers to Nanometers
fun hectometersToNanometers(hectometers: Double): Double {
    return hectometers * 100000000000
}

// Inches to Nanometers
fun inchesToNanometers(inches: Double): Double {
    return inches * 25400000
}

// Kilometers to Nanometers
fun kilometersToNanometers(kilometers: Double): Double {
    return kilometers * 1000000000000
}

// Meters to Nanometers
fun metersToNanometers(meters: Double): Double {
    return meters * 1000000000
}

// Micrometers to Nanometers
fun micrometersToNanometers(micrometers: Double): Double {
    return micrometers * 1000
}

// Miles to Nanometers
fun milesToNanometers(miles: Double): Double {
    return miles * 1609344000000
}

// Millimeters to Nanometers
fun millimetersToNanometers(millimeters: Double): Double {
    return millimeters * 1000000
}

// Nautical Miles to Nanometers
fun nauticalMilesToNanometers(nauticalMiles: Double): Double {
    return nauticalMiles * 1852001000000
}

// Picometers to Nanometers
fun picometersToNanometers(picometers: Double): Double {
    return picometers * 0.001
}

// Yards to Nanometers
fun yardsToNanometers(yards: Double): Double {
    return yards * 914400000
}