package dev.calculate_engine.math.conversion

// Inch Conversion

// Centimeters to Inches
fun centimetersToInches(centimeters: Double): Double {
    return centimeters * 0.3937008
}

// Decameters to Inches
fun decametersToInches(decameters: Double): Double {
    return decameters * 393.7008
}

// Decimeters to Inches
fun decimetersToInches(decimeters: Double): Double {
    return decimeters * 3.937008
}

// Feet to Inches
fun feetToInches(feet: Double): Double {
    return feet * 12
}

// Hectometers to Inches
fun hectometersToInches(hectometers: Double): Double {
    return hectometers * 3937.008
}

// Kilometers to Inches
fun kilometersToInches(kilometers: Double): Double {
    return kilometers * 39370.08
}

// Meters to Inches
fun metersToInches(meters: Double): Double {
    return meters * 39.37008
}

// Micrometers to Inches
fun micrometersToInches(micrometers: Double): Double {
    return micrometers * 0.00003937008
}

// Miles to Inches
fun milesToInches(miles: Double): Double {
    return miles * 63360
}

// Millimeters to Inches
fun millimetersToInches(millimeters: Double): Double {
    return millimeters * 0.03937008
}

// Nanometers to Inches
fun nanometersToInches(nanometers: Double): Double {
    return nanometers * 0.00000003937008
}

// Nautical Miles to Inches
fun nauticalMilesToInches(nauticalMiles: Double): Double {
    return nauticalMiles * 72913.42
}

// Picometers to Inches
fun picometersToInches(picometers: Double): Double {
    return picometers * 0.00000000003937008
}

// Yards to Inches
fun yardsToInches(yards: Double): Double {
    return yards * 36
}