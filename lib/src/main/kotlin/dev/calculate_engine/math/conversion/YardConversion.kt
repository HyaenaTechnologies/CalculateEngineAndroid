package dev.calculate_engine.math.conversion

// Yard Conversion

// Centimeters to Yards
fun centimetersToYards(centimeters: Double): Double {
    return centimeters * 0.01093613
}

// Decameters to Yards
fun decametersToYards(decameters: Double): Double {
    return decameters * 10.93613
}

// Decimeters to Yards
fun decimetersToYards(decimeters: Double): Double {
    return decimeters * 0.1093613
}

// Feet to Yards
fun feetToYards(feet: Double): Double {
    return feet * 0.3333333
}

// Hectometers to Yards
fun hectometersToYards(hectometers: Double): Double {
    return hectometers * 109.3613
}

// Inches to Yards
fun inchesToYards(inches: Double): Double {
    return inches * 0.02777778
}

// Kilometers to Yards
fun kilometersToYards(kilometers: Double): Double {
    return kilometers * 1093.613
}

// Meters to Yards
fun metersToYards(meters: Double): Double {
    return meters * 1.093613
}

// Micrometers to Yards
fun micrometersToYards(micrometers: Double): Double {
    return micrometers * 0.000001093613
}

// Miles to Yards
fun milesToYards(miles: Double): Double {
    return miles * 1760
}

// Millimeters to Yards
fun millimetersToYards(millimeters: Double): Double {
    return millimeters * 0.001093613
}

// Nanometers to Yards
fun nanometersToYards(nanometers: Double): Double {
    return nanometers * 0.000000001093613
}

// Nautical Miles to Yards
fun nauticalMilesToYards(nauticalMiles: Double): Double {
    return nauticalMiles * 2025.373
}

// Picometers to Yards
fun picometersToYards(picometers: Double): Double {
    return picometers * 0.000000000001093613
}