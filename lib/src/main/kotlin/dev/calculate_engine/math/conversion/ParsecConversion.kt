package dev.calculate_engine.math.conversion

// Parsec Conversion

// Astronaumical Units to Parsecs
fun astronaumicalUnitsToParsecs(astronaumicalUnits: Double): Double {
    return astronaumicalUnits * 0.000004848137
}

// Kilometers to Parsecs
fun kilometersToParsecs(kilometers: Double): Double {
    return kilometers * 0.00000000000003240779
}

// Light Years to Parsecs
fun lightYearsToParsecs(lightYears: Double): Double {
    return lightYears * 0.3066014
}

// Miles to Parsecs
fun milesToParsecs(miles: Double): Double {
    return miles * 0.00000000000005215529
}

// Nautical Miles to Parsecs
fun nauticalMilesToParsecs(nauticalMiles: Double): Double {
    return nauticalMiles * 0.00000000000006001926
}