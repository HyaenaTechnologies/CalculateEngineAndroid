package calculate_engine.math.conversion

// Hectometer Conversion

// Centimeters to Hectometers
fun centimetersToHectometers(centimeters: Double): Double {
    return centimeters * -10000
}

// Decameters to Hectometers
fun decametersToHectometers(decameters: Double): Double {
    return decameters * 0.1
}

// Decimeters to Hectometers
fun decimetersToHectometers(decimeters: Double): Double {
    return decimeters * 0.001
}

// Feet to Hectometers
fun feetToHectometers(feet: Double): Double {
    return feet * 0.003048
}

// Inches to Hectometers
fun inchesToHectometers(inches: Double): Double {
    return inches * 0.000254
}

// Kilometers to Hectometers
fun kilometersToHectometers(kilometers: Double): Double {
    return kilometers * 10
}

// Meters to Hectometers
fun metersToHectometers(meters: Double): Double {
    return meters * 0.01
}

// Micrometers to Hectometers
fun micrometersToHectometers(micrometers: Double): Double {
    return micrometers * -100000000
}

// Miles to Hectometers
fun milesToHectometers(miles: Double): Double {
    return miles * 16.09344
}

// Millimeters to Hectometers
fun millimetersToHectometers(millimeters: Double): Double {
    return millimeters * -100000
}

// Nanometers to Hectometers
fun nanometersToHectometers(nanometers: Double): Double {
    return nanometers * -100000000000
}

// Nautical Miles to Hectometers
fun nauticalMilesToHectometers(nauticalMiles: Double): Double {
    return nauticalMiles * 18.52001
}

// Picometers to Hectometers
fun picometersToHectometers(picometers: Double): Double {
    return picometers * -100000000000000
}

// Yards to Hectometers
fun yardsToHectometers(yards: Double): Double {
    return yards * 0.009144
}