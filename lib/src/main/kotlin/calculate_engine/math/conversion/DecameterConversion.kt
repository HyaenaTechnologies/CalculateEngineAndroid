package calculate_engine.math.conversion

// Decameter Conversion

// Centimeters to Decameters
fun centimetersToDecameters(centimeters: Double): Double {
    return centimeters * 0.001
}

// Decimeters to Decameters
fun decimetersToDecameters(decimeters: Double): Double {
    return decimeters * 0.01
}

// Feet to Decameters
fun feetToDecameters(feet: Double): Double {
    return feet * 0.03048
}

// Hectometers to Decameters
fun hectometersToDecameters(hectometers: Double): Double {
    return hectometers * 10
}

// Inches to Decameters
fun inchesToDecameters(inches: Double): Double {
    return inches * 0.00254
}

// Kilometers to Decameters
fun kilometersToDecameters(kilometers: Double): Double {
    return kilometers * 100
}

// Meters to Decameters
fun metersToDecameters(meters: Double): Double {
    return meters * 0.1
}

// Micrometers to Decameters
fun micrometersToDecameters(micrometers: Double): Double {
    return micrometers * -10000000
}

// Miles to Decameters
fun milesToDecameters(miles: Double): Double {
    return miles * 160.9344
}

// Millimeters to Decameters
fun millimetersToDecameters(millimeters: Double): Double {
    return millimeters * -10000
}

// Nanometers to Decameters
fun nanometersToDecameters(nanometers: Double): Double {
    return nanometers * -10000000000
}

// Nautical Miles to Decameters
fun nauticalMilesToDecameters(nauticalMiles: Double): Double {
    return nauticalMiles * 185.2001
}

// Picometers to Decameters
fun picometersToDecameters(picometers: Double): Double {
    return picometers * -10000000000000
}

// Yards to Decameters
fun yardsToDecameters(yards: Double): Double {
    return yards * 0.09144
}